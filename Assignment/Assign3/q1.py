#1 เขียนฟังก์ชันเพื่อบวกตัวเลข 3 ตัว และ return ผลลัพธ์ออกมา กำหนดให้ฟังก์ชันนี้รับค่า
# parameter 3 ตัว คือ a,b,c ที่เป็นจำนวนใดๆ

